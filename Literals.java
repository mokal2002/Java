//Literals in java

public class Literals {

    public static void main(String[] args) {
        
        
        long l=9999999999L;  //The long data type is a 64-bit two's complement integer.00L
        float f=12.56f;      //The default value of a float in Java is 0.0f.00.00f
        double d=12.56d;     //00.00d
        
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }
    
}



/////////Output/////////
// 9999999999         //
// 12.56              //
// 12.56              //
